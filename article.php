                <br/>
                <strong>媚术师妖娆魅力 颠覆人族印象</strong><br/>
                <br/>
                在游戏中，人族职业向来是单体物攻和治疗系的象征。人族的“贵族剑士”以其骄傲的“风华连舞”这一单体多次强攻技能，成为当之无愧的BOSS终结者；而“圣女”作为游戏中唯一的治愈系职业，不仅拥有群体HP、MP恢复技能，更有副本战斗、
