                <br/>
                然而，也正是因为如此，人族也给外界留下了“法力欠缺”的印象，没有法术攻击的加持，人族团队始终无法在PK赛中占据绝对优势。<br/>
                <br/>
                而岁这9月28日新资料片公测，人族的星光职业“媚术师”将正式与玩家见面。“媚术师”的多项禁锢技能将撼动同样拥有封系法术的精灵族和血族。其封系技能不仅能够限制对手的法术攻击以及物理攻击，更可以限制攻击回合数，让对手只有“默默挨打”的份！<br/>
                <br/>
                <strong>血族新贵华丽暴走 外形不怒自威</strong><br/>
                <br/>
                血影贵族是《梦幻迪士尼》爆发力最强的职业！他们对力量有着狂热的崇拜，追求的是身体与力量的完美结合。“血影舞者”敏捷的身手以及“血影领主”令人畏惧的魔力暴走，让血族成为最受玩家欢迎的种族之一。而“血影贵族”的出现，则将血族的高傲冷酷更加深化，在特定的情况下，血影贵族会变身为另一种形态，陷入狂暴的他们力量大增，能给予多个对手致命的伤害。<br/>
                <br/>
                血影贵族还是爱宠人士。他的职业特色是在喂养伙伴时加倍提升伙伴的忠诚度，并且伙伴死亡后不掉忠诚度。这就决定了血影贵族可以更好的控制伙伴为其战斗，是《梦幻迪士尼》中强大的训宠师！<br/>
                <br/>
